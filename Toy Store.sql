--7. Highest-demand Product Across All Locations (by Units Sold)--


CREATE VIEW HIGHEST_DEMAND_PRODUCT AS

SELECT TOP 1
    P.PRODUCT_ID, P.PRODUCT_NAME, P.PRODUCT_CATEGORY,
    SUM(S.UNITS) AS TOTAL_UNITS_SOLD, SUM(S.UNITS*P.PRODUCT_RETAIL_PRICE) AS TOTAL_SALES_AMOUNT
FROM SALES S
JOIN PRODUCTS P ON S.Product_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.PRODUCT_NAME, P.PRODUCT_CATEGORY
ORDER BY TOTAL_UNITS_SOLD DESC;


--FINAL ASNWER--

SELECT * FROM HIGHEST_DEMAND_PRODUCT;


--8) Inventory Health Ś Average Inventory Per Store & Per Product (and Simple Turnover)--

CREATE VIEW INVENTORY_HEALTH AS

SELECT
    I.STORE_ID, ST.STORE_NAME, I.PRODUCT_ID, P.PRODUCT_NAME, 
    AVG(I.STOCK_ON_HAND) AS AVG_STOCK_ON_HAND,
    SUM(S.UNITS) AS TOTAL_UNITS_SOLD,
    CASE
        WHEN AVG(I.STOCK_ON_HAND) > 0 THEN
            SUM(S.UNITS) / AVG(I.STOCK_ON_HAND)
        ELSE
            NULL
    END AS INVENTORY_TURNOVER_RATIO
FROM INVENTORY I
JOIN STORES ST ON I.STORE_ID = ST.STORE_ID
JOIN PRODUCTS P ON I.PRODUCT_ID = P.PRODUCT_ID
LEFT JOIN SALES S ON S.STORE_ID = I.STORE_ID AND S.PRODUCT_ID = I.PRODUCT_ID AND YEAR(S.DATE) = 2023
GROUP BY I.STORE_ID, ST.STORE_NAME, I.PRODUCT_ID, P.PRODUCT_NAME
ORDER BY INVENTORY_TURNOVER_RATIO DESC
OFFSET 0 ROWS;

--FINAL ANSWER--

SELECT * FROM INVENTORY_HEALTH;